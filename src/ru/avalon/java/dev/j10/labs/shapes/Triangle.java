/**
 * Представление о треугольнике.
 * <p>
 * Треуго́льник (в евклидовом пространстве) — геометрическая
 * фигура, образованная тремя отрезками, которые соединяют
 * три точки, не лежащие на одной прямой. Указанные три
 * точки называются вершинами треугольника, а отрезки —
 * сторонами треугольника. Часть плоскости, ограниченная
 * сторонами, называется внутренностью треугольника: нередко
 * треугольник рассматривается вместе со своей внутренностью
 * (например, для определения понятия площади).
 *
 * @see <a href="https://ru.wikipedia.org/wiki/%D0%A2%D1%80%D0%B5%D1%83%D0%B3%D0%BE%D0%BB%D1%8C%D0%BD%D0%B8%D0%BA">Треугольник</a>
 */
public class Triangle implements Polygon {
    public float side1, side2, side3, trianglePerimetr, triangleArea; 
    

    /*
     * TODO: Реализовать класс 'Triangle'
     * 1. Используйте наследование.
     * 2. Реализуйте все абстрактные методы.
     */

    public Triangle(float side1, float side2, float side3) {
        this.side1 = side1;
        this.side2 = side2;
        this.side3 = side3;
        trianglePerimetr = side1 + side2 + side3;
        float halfP = trianglePerimetr/2;
        
        float temp = (float)Math.abs((halfP *(halfP - side1)*(halfP - side2)*(halfP - side3)));
        triangleArea = (float)Math.sqrt(temp);
    }

    @Override
    public float getPerimeter() {
        return trianglePerimetr;
    }

    @Override
    public float getArea() {
        return triangleArea;
    }
}
